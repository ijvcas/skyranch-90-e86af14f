
import { supabase } from '@/integrations/supabase/client';

export interface SyncResult {
  action: string;
  returned_lot_id: string;
  parcel_id: string;
  lot_name: string;
}

export const syncCadastralParcelsToLots = async (): Promise<SyncResult[]> => {
  try {
    console.log('ğŸ”„ Starting sync of PROPIEDAD parcels to lots...');
    
    // First check if we have any PROPIEDAD parcels
    const { data: parcelsCheck, error: parcelsError } = await supabase
      .from('cadastral_parcels')
      .select('id, parcel_id, status')
      .eq('status', 'PROPIEDAD');
    
    if (parcelsError) {
      console.error('âŒ Error checking PROPIEDAD parcels:', parcelsError);
      throw parcelsError;
    }
    
    console.log(`ğŸ“Š Found ${parcelsCheck?.length || 0} PROPIEDAD parcels before sync`);
    
    if (!parcelsCheck || parcelsCheck.length === 0) {
      console.warn('âš ï¸ No PROPIEDAD parcels found to sync');
      return [];
    }
    
    // Call the database function
    const { data, error } = await supabase.rpc('sync_propiedad_parcels_to_lots');
    
    if (error) {
      console.error('âŒ Database function error:', error);
      console.error('âŒ Error details:', {
        message: error.message,
        details: error.details,
        hint: error.hint,
        code: error.code
      });
      throw error;
    }
    
    console.log(`âœ… Sync function executed successfully. Results:`, data);
    console.log(`ğŸ“ˆ Number of operations performed: ${data?.length || 0}`);
    
    // Log each result for debugging and map returned_lot_id to lot_id for compatibility
    if (data && data.length > 0) {
      const mappedResults = data.map((result: any, index: number) => {
        console.log(`ğŸ“‹ Operation ${index + 1}:`, {
          action: result.action,
          lot_id: result.returned_lot_id,
          parcel_id: result.parcel_id,
          lot_name: result.lot_name
        });
        
        // Map returned_lot_id to lot_id for backward compatibility
        return {
          action: result.action,
          lot_id: result.returned_lot_id,
          parcel_id: result.parcel_id,
          lot_name: result.lot_name
        };
      });
      
      return mappedResults;
    }
    
    return data || [];
  } catch (error) {
    console.error('âŒ Unexpected error in syncCadastralParcelsToLots:', error);
    if (error instanceof Error) {
      console.error('âŒ Error message:', error.message);
      console.error('âŒ Error stack:', error.stack);
    }
    throw error;
  }
};

export const getAutoGeneratedLotsInfo = async () => {
  try {
    console.log('ğŸ” Fetching auto-generated lots info...');
    
    const { data, error } = await supabase
      .from('lots')
      .select(`
        id,
        name,
        auto_generated,
        source_parcel_id,
        cadastral_parcels!source_parcel_id(parcel_id, status)
      `)
      .eq('auto_generated', true);
    
    if (error) {
      console.error('âŒ Error fetching auto-generated lots info:', error);
      throw error;
    }
    
    console.log(`ğŸ“Š Found ${data?.length || 0} auto-generated lots`);
    return data || [];
  } catch (error) {
    console.error('âŒ Error in getAutoGeneratedLotsInfo:', error);
    throw error;
  }
};
